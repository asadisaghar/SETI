class Halo(object):
    def __init__(self, particle_number, radius, mass):
        self.particle_no = particle_number
        self.radius = radius
        self.mass = mass

class Dark(self, Halo, vel_disp):
    self.vel_disp = vel_disp

class Stellar(self. Halo, luminosity):
    self.luminosity = luminosity
    
